module half_adder(
    input I_a,      //加数1
    input I_b,      //加数2
    
    output O_sum,   //和
    output O_cout   //进位信号
);


//*********************************PROCESS**************************************
// FUNCTION        :半加法器的逻辑实现
//****************************************************************************** 
    assign O_sum = I_a ^ I_b;
    assign O_cout = I_a & I_b;
    
endmodule


